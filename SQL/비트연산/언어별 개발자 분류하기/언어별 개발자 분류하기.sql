
SELECT 
CASE
    WHEN A.SKILL_CODE & (SELECT SUM(CODE) FROM SKILLCODES WHERE NAME = 'Python') > 0 AND A.SKILL_CODE & (SELECT SUM(CODE) FROM SKILLCODES WHERE CATEGORY = 'Front End') > 0 THEN 'A'
    WHEN A.SKILL_CODE & (SELECT SUM(CODE) FROM SKILLCODES WHERE NAME = 'C#') > 0 THEN 'B'
    WHEN A.SKILL_CODE & (SELECT SUM(CODE) FROM SKILLCODES WHERE CATEGORY = 'Front End') > 0 THEN 'C'
    ELSE NULL
END AS GRADE, ID, EMAIL
FROM DEVELOPERS A
HAVING GRADE IS NOT NULL
ORDER BY GRADE, ID;


